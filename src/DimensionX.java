
public class DimensionX {
	int x, y, length, width;
	
	public DimensionX(){
		x = 0;
		y = 0;
		length = 1;
		width = 1;
	}//end default constructor
	
	public DimensionX(int x, int y, int length, int width){
		this.x = x;
		this.y = y;
		this.length = length;
		this.width = width;
	}//end constructor
}//end Class
